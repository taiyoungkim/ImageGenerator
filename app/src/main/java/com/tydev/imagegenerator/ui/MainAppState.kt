package com.tydev.imagegenerator.ui

import androidx.compose.material3.windowsizeclass.WindowSizeClass
import androidx.compose.material3.windowsizeclass.WindowWidthSizeClass
import androidx.compose.runtime.Composable
import androidx.compose.runtime.Stable
import androidx.compose.runtime.remember
import androidx.navigation.NavDestination
import androidx.navigation.NavGraph.Companion.findStartDestination
import androidx.navigation.NavHostController
import androidx.navigation.compose.currentBackStackEntryAsState
import androidx.navigation.compose.rememberNavController
import androidx.navigation.navOptions
import androidx.tracing.trace
import com.tydev.imagegenerator.feature.generator.navigation.GeneratorNavigationRoute
import com.tydev.imagegenerator.feature.generator.navigation.navigateToGenerator
import com.tydev.imagegenerator.feature.setting.navigation.navigateToSetting
import com.tydev.imagegenerator.navigation.TopLevelDestination
import com.tydev.imagegenerator.navigation.TopLevelDestination.GENERATOR
import com.tydev.imagegenerator.navigation.TopLevelDestination.SETTING

@Composable
fun rememberMainAppState(
    windowSizeClass: WindowSizeClass,
    navController: NavHostController = rememberNavController(),
): MainAppState {
    return remember(navController, windowSizeClass) {
        MainAppState(navController, windowSizeClass)
    }
}

@Stable
class MainAppState(
    val navController: NavHostController,
    val windowSizeClass: WindowSizeClass,
) {
    val currentDestination: NavDestination?
        @Composable get() = navController
            .currentBackStackEntryAsState().value?.destination

    val currentTopLevelDestination: TopLevelDestination?
        @Composable get() = when (currentDestination?.route) {
            GeneratorNavigationRoute -> GENERATOR
            else -> null
        }

    val shouldShowBottomBar: Boolean
        get() = windowSizeClass.widthSizeClass == WindowWidthSizeClass.Compact

    val shouldShowNavRail: Boolean
        get() = !shouldShowBottomBar

    val topLevelDestinations: List<TopLevelDestination> = TopLevelDestination.values().asList()

    fun navigateToTopLevelDestination(topLevelDestination: TopLevelDestination) {
        trace("Navigation: ${topLevelDestination.name}") {
            val topLevelNavOptions = navOptions {
                // Pop up to the start destination of the graph to
                // avoid building up a large stack of destinations
                // on the back stack as users select items
                popUpTo(navController.graph.findStartDestination().id) {
                    saveState = true
                }
                // Avoid multiple copies of the same destination when
                // reselecting the same item
                launchSingleTop = true
                // Restore state when reselecting a previously selected item
                restoreState = true
            }

            when (topLevelDestination) {
                GENERATOR -> navController.navigateToGenerator(topLevelNavOptions)
                SETTING -> navController.navigateToSetting(topLevelNavOptions)
            }
        }
    }
}
