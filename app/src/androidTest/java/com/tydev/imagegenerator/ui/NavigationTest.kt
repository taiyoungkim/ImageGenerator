package com.tydev.imagegenerator.ui

import android.app.Application
import androidx.annotation.StringRes
import androidx.compose.ui.test.assertIsSelected
import androidx.compose.ui.test.junit4.AndroidComposeTestRule
import androidx.compose.ui.test.junit4.createAndroidComposeRule
import androidx.compose.ui.test.onNodeWithText
import com.tydev.imagegenerator.MainActivity
import dagger.hilt.android.testing.CustomTestApplication
import dagger.hilt.android.testing.HiltAndroidRule
import dagger.hilt.android.testing.HiltAndroidTest
import org.junit.Rule
import org.junit.Test
import kotlin.properties.ReadOnlyProperty
import com.tydev.imagegenerator.feature.generator.R as FeatureGeneratorR

@CustomTestApplication(Application::class)
@HiltAndroidTest
class NavigationTest {

    /**
     * Manages the components' state and is used to perform injection on your test
     */
    @get:Rule(order = 0)
    val hiltRule = HiltAndroidRule(this)

    /**
     * Use the primary activity to initialize the app normally.
     */
    @get:Rule(order = 1)
    val composeTestRule = createAndroidComposeRule<MainActivity>()

    private fun AndroidComposeTestRule<*, *>.stringResource(@StringRes resId: Int) =
        ReadOnlyProperty<Any?, String> { _, _ -> activity.getString(resId) }

    private val generator by composeTestRule.stringResource(FeatureGeneratorR.string.generator)

    @Test
    fun firstScreen_isGenerator() {
        composeTestRule.apply {
            // VERIFY for you is selected
            onNodeWithText(generator).assertIsSelected()
        }
    }
}
